package dev.ropimasi.curso.especialistajava.modulo05.desafio_35;

public class AreasCalculadora {


    public static double PI = 3.14159265356979;

    public static double doQuadrado(double lado) {
        return lado * lado;
    }


    public static double doCirculo(double raio) {
        return AreasCalculadora.PI * raio * raio;
    }


}
